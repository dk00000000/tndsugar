<p>&nbsp;&nbsp;&nbsp;मी या वाहनाने सन <u><strong>2018-19 </strong></u>चे गळीत हंगामात तोडणी वाहतूकीचे काम करणेसाठी श्री अंबालिका शुगर प्रा.लिमिटेड अंबिकानगर या साखर कारखान्याच्या परीसरात येऊन ऊस ऊत्पादकाकडून त्यांचे ऊसाचे तोडणी वाहतूकीचे काम मिळविणार आहे. ऊस तोडणी वाहतुकीचे काम करणारांना तुम्ही मदत करीत असता. ऊसाचे तोडणी वाहतूकीचे काम ऊस ऊत्पादकाकडून मिळवून देणेकामी व माझे ऊसाचे तोडणी वाहतूकीचे काम सुरळीत चालावे म्हणून तुम्ही आम्हास मदत व सहकार्य देण्याचे मान्य केलेले आहे. ज्या ऊस ऊत्पादकाचे ऊसाचे तोडणी वाहतूकीचे काम मी करीन त्यांचेकडून मला त्योचेशी ठरविलेल्या दराने तोडणी वाहतुकीची रक्क्म मिळणार आहे. मी करणार असलेल्या ऊस तोडणी वाहतूकीचे कामात तुमचेकडे जी मदत व सहकार्य मिळणार आहे. त्यासठी हा करार तुम्हांस लिहून देत आहे.</p>
