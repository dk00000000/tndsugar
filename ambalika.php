<p>पुर्वतयारीसाठी तसेच गळीत हंगाम चालु असताना तुमचेकडुन जी ॲड्व्हान्सची रक्कम घेतली असेल ती माझे होणारे कामाचे बिलातुन तुम्ही वेळोवेळी कापुन घ्यावी तसा तुम्हास अधिकार दिलेला आहे. ॲड्व्हान्स रक्कमेमुळे माझे कामात तुमची मोठी मदत होणार आहे. गळीत हंगाम आखेर पर्यंत वेगवेगळ्या ऊस पुरवठादाराबरोबर ऊस तोडणी वाहतूकिचे करार करुन या कामाचे बिलातुन तुमचेकडुन घेतलेल्या ॲड्व्हान्स रक्कमेची मी फेड करील. ॲड्व्हान्स घेऊन कारखान्याच्या परीसरात तोडणी वाहतुकिचे काम करण्यास न आल्यास अगर आल्यानंतर तुमचे ॲड्व्हान्सची फेड न होताच गळीत हंगाम संपणेचे आत तोडणी वाहतुकिचे काम मिळविण्याचे व करण्याचे सोडुन गेल्यास ती बाब मी स्वतः केलेली तुमची फसवणुक ठरेल. मदत म्हणुन ॲड्व्हान्स घेऊन कारखाना परीसरात कामावर न येता अगर कामावर आल्याचे दाखवुन मध्यंतरी काम सोडुन देऊन ॲड्व्हान्स फेडीबाबत तुमची फसवणुक करणार नाही. तसे घड्ल्यास तुमची मी स्वतः केलेली फसवणुक ठरेल. फसवणुकीने व लबाडीने ॲड्व्हानसची रक्कम तुमचेकडुन घेतली व मदतीचा गैरफायदा घेतला असे होईल व राहील. त्यावेळी मी फौजदारी गुन्ह्यास पात्र राहील. तुम्हांस माझे विरुद्ध यावेळी मी फौजदारीचा गुन्हा केला असल्यामुळे फसवणुक वगैरेचा फौजदारी खट्ला दाखल करता येईल.</p>
